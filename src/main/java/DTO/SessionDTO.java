package DTO;

public class SessionDTO {
    private String token;
    private int id;

    public SessionDTO() {
    }

    public SessionDTO(int id) {
        this.id = id;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
